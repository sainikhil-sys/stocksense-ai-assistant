import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers": "authorization, x-client-info, apikey, content-type, x-supabase-client-platform, x-supabase-client-platform-version, x-supabase-client-runtime, x-supabase-client-runtime-version",
};

serve(async (req) => {
  if (req.method === "OPTIONS") return new Response(null, { headers: corsHeaders });

  try {
    const { messages } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    if (!LOVABLE_API_KEY) throw new Error("LOVABLE_API_KEY is not configured");

    const systemPrompt = `You are StockSense AI — an expert stock market analyst and advisor for Indian retail investors.

Your capabilities:
- Analyze stocks using technical indicators (RSI, MACD, Moving Averages, Volume)
- Provide Buy/Sell/Hold recommendations with confidence scores (0-100)
- Suggest entry/exit prices and risk levels
- Explain market trends and patterns in simple language
- Analyze sector performance and market sentiment

When analyzing a stock, always provide:
1. **Recommendation**: BUY / SELL / HOLD
2. **Confidence Score**: 0-100%
3. **Entry Price**: Suggested buy price
4. **Target/Exit Price**: Expected price target
5. **Stop Loss**: Risk management level
6. **Risk Level**: LOW / MEDIUM / HIGH
7. **Reasoning**: Technical and fundamental analysis

Use Indian market context (NSE/BSE). Reference NIFTY, SENSEX indices. Use ₹ for prices.
Be concise but thorough. Use markdown formatting for readability.
If you don't have real-time data, clearly state your analysis is based on general patterns and historical context.`;

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-3-flash-preview",
        messages: [
          { role: "system", content: systemPrompt },
          ...messages,
        ],
      }),
    });

    if (!response.ok) {
      if (response.status === 429) {
        return new Response(JSON.stringify({ error: "Rate limit exceeded. Please try again shortly." }), {
          status: 429,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      if (response.status === 402) {
        return new Response(JSON.stringify({ error: "AI credits exhausted. Please add funds in Settings > Workspace > Usage." }), {
          status: 402,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      const t = await response.text();
      console.error("AI gateway error:", response.status, t);
      throw new Error("AI gateway error");
    }

    const data = await response.json();

    return new Response(JSON.stringify(data), {
      headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  } catch (e) {
    console.error("chat error:", e);
    return new Response(JSON.stringify({ error: e instanceof Error ? e.message : "Unknown error" }), {
      status: 500,
      headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  }
});
